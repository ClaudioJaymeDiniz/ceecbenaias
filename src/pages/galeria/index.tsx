export default function Galeria() {
    const [modalOpen, setModalOpen] = useState(false);
    const [selectedImage, setSelectedImage] = useState('');

    const handleImageClick = (src: SetStateAction<string>) => {
        setSelectedImage(src);
        setModalOpen(true);
    };

    const closeModal = () => {
        setModalOpen(false);
        setSelectedImage('');
    };

    return (
        <div>
            <div className="columns-1 md:columns-2 xl:columns-3 px-4 gap-7 galeria pt-4">
                {[imagem1, imagem2, imagem3, imagem4, imagem5, imagem6, imagem8, imagem9, imagem10, imagem11, imagem12, imagem13,imagem14,imagem15,imagem16,imagem17,imagem18,
                    imagem19,imagem20,imagem21,imagem22,imagem23,imagem24,imagem25,imagem26,imagem27,imagem28
                    
                ].map((src, idx) => (
                    <div key={idx} className="group cursor-pointer relative" onClick={() => handleImageClick(src)}>
                        <img
                            src={src}
                            alt={`Image ${idx + 1}`}
                            className="h-auto max-w-full my-7 rounded-lg transition-transform transform scale-100 group-hover:scale-105"
                        />
                        
                    </div>
                ))}
            </div>
            
            {modalOpen && (
                <div className="fixed inset-0 bg-black bg-opacity-75 flex items-center justify-center z-50">
                    <div className="bg-white rounded-lg overflow-hidden">
                        <button className="absolute top-2 right-2 text-white px-2 py-1 rounded-full" onClick={closeModal}>
                            X
                        </button>
                        <img src={selectedImage} alt="Selected" className="foto max-w-full  max-h-screen " />
                    </div>
                </div>
            )}
        </div>
    );
}



